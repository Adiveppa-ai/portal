CREATE OR REPLACE PROCEDURE CMT_COPY_GROUP_PLANLIST(
  			p_stage_dbno NUMBER,
                        p_return_code OUT NUMBER,
                        p_return_mesg OUT VARCHAR2
                        )
IS
BEGIN

INSERT INTO  GROUP_T
(POID_DB,
 POID_ID0,
 POID_TYPE,
 POID_REV,
 CREATED_T,
 MOD_T,
 READ_ACCESS,
 WRITE_ACCESS,
 ACCOUNT_OBJ_DB,
 ACCOUNT_OBJ_ID0,
 ACCOUNT_OBJ_TYPE,
 ACCOUNT_OBJ_REV,
 COUNT,
 DESCR,
 NAME,
 PARENT_DB,
 PARENT_ID0,
 PARENT_TYPE,
 PARENT_REV,
 BAL_GRP_OBJ_DB,
 BAL_GRP_OBJ_ID0,
 BAL_GRP_OBJ_TYPE,
 BAL_GRP_OBJ_REV,
 STATUS,
 TYPE_STR)
(SELECT p_stage_dbno,
 POID_ID0,
 POID_TYPE,
 POID_REV,
 CREATED_T,
 MOD_T,
 READ_ACCESS,
 WRITE_ACCESS,
 ACCOUNT_OBJ_DB,
 ACCOUNT_OBJ_ID0,
 ACCOUNT_OBJ_TYPE,
 ACCOUNT_OBJ_REV,
 COUNT,
 DESCR,
 NAME,
 PARENT_DB,
 PARENT_ID0,
 PARENT_TYPE,
 PARENT_REV,
 BAL_GRP_OBJ_DB,
 BAL_GRP_OBJ_ID0,
 BAL_GRP_OBJ_TYPE,
 BAL_GRP_OBJ_REV,
 STATUS,
 TYPE_STR from group_t where poid_type ='/group/plan_list');

  commit;

EXCEPTION
WHEN OTHERS THEN
        p_return_code := 1;
        p_return_mesg := 'IN OTHERS OF CONV_MAIN_TABLES_RECOVERY '|| SQLERRM;
END;
/
