/**
 * \defgroup xmlstreams_module XML Streams Module
 * \ingroup sourcepro_core_product
 *
 * The XML Streams Module enables C++ data to be written to or read from an
 * XML stream. It builds on the classes of the Advanced Tools Module. With
 * XML Streams, you can turn instances of C++ classes into XML without
 * writing XML streaming code, making it easier to integrate existing C++
 * code into systems that use XML to communicate.
 */

/**
 * \defgroup xmlstreams_package XML Streams
 * \ingroup xmlstreams_module
 *
 * These classes allow you to serialize an object and write it as a stream
 * of XML data. The serialized object can later be streamed in and restored,
 * either by the process that streamed the object or by another process
 * equipped to handle XML streams.
 */

